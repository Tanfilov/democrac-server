// Generate the structured politician list
function generatePoliticiansList() {
  const politicians = [];
  
  // Process each party and add politicians
  for (const partyData of partiesAndPoliticians) {
    const partyName = partyData.party;
    
    for (const politicianName of partyData.politicians) {
      politicians.push({
        name: politicianName,
        // Leave party field empty for ministers who are not Knesset members
        party: partyName === "שרים שאינם חברי כנסת" ? "" : partyName,
        position: "",
        aliases: [],
        image: "",
        image_url: ""
      });
    }
  }
  
  // Write to file
  fs.writeFileSync(
    path.join(__dirname, 'politicians_new.json'),
    JSON.stringify(politicians, null, 2)
  );
  
  console.log(`Created politicians_new.json with ${politicians.length} politicians`);
}

// Run the function
